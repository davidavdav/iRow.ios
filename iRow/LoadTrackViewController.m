    self = [super initWithStyle:style];
    if (self) {
        // Custom initialization
        dir = [[[NSFileManager defaultManager] URLsForDirectory:NSDocumentDirectory inDomains:NSUserDomainMask] lastObject];
        NSError * error;
        NSPredicate * predicate = [NSPredicate predicateWithFormat:@"SELF MATCHES '.*\\.iRowTrack'"];
        tracks = [[[NSFileManager defaultManager] contentsOfDirectoryAtPath:[dir path] error:&error] filteredArrayUsingPredicate:predicate];
        NSLog(@"%@", tracks);
    }
    return self;
}

