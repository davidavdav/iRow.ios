#pragma mark - Table view delegate

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    NSString * path = [[dir URLByAppendingPathComponent:[tracks objectAtIndex:indexPath.row]] path];
    TrackExport * export = [NSKeyedUnarchiver unarchiveObjectWithFile:path];
    Track * new = (Track*)[NSEntityDescription insertNewObjectForEntityForName:@"Track" inManagedObjectContext:Settings.sharedInstance.moc];
    [new importFrom:export];
    NSError * error;
    [Settings.sharedInstance.moc save:&error];
    [self.navigationController popViewControllerAnimated:YES];
}

